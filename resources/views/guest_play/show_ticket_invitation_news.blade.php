    <h4 class="text-center mb-4">Guest Invitation</h4>
    <p class="text-left">Kjære gjest,</p>
    <p class="text-left">Velkommen til Oustøen Country Club. Vi håper du nyter dagen med oss på øya.</p>
    <p class="text-left">Vennligst les denne viktige informasjonen før du besøker klubben.</p>

    <h6 class="font-weight-bold">INNSJEKKING / REGISTRERING</h6>
    <p class="text-left">Ved ankomst vennligst sjekk inn/registrer deg i kiosken i klubbhuset og oppgi navnet på
        medlemmet du skal spille med.</p>

    <h6 class="font-weight-bold">KLESKODE I KLUBBHUS / TERRASSEN</h6>
    <p class="text-left">Antrekket skal være ‘smart casual’ og helt, rent og pent.</p>

    <h6 class="font-weight-bold">KLESKODE PÅ GOLFBANEN - HERRER</h6>
    <p class="text-left">Antrekket på golfbanen skal være golfspesifikke - skjorter med krage og bukser eller shorts
        med belte er påkrevd. Store logoer, slagord, tall eller illustrasjoner på klær som ikke er
        golfrelatert, samt badeshorts, treningstøy og singlet er ikke tillatt.</p>

    <h6 class="font-weight-bold">KLESKODE PÅ GOLFBANEN – DAMER</h6>
    <p class="text-left">Antrekket på golfbanen - skjorter bukser, skjørt, kjole, shorts og skorts skal være skal være
        golfspesifikke.</p>

    <h6 class="font-weight-bold">SPILLEHASTIGHET</h6>
    <p class="text-left">En fireball bør ikke ta mer enn 4 timer for 18 hull. Det forventes att du holde kontakten med
        gruppen foran, og slipp raskere grupper forbi.</p>

    <h6 class="font-weight-bold">MOBILTELEFON</h6>
    <p class="text-left">OCC praktiserer mobilvett. Telefonen skal til enhver tid være lydløs.
        Dvs. at din bruk av mobiltelefon skjer under betingelse av at ingen påvirkes av dette, eller at
        det sinker spillet.</p>

    <h6 class="font-weight-bold">FERGE</h6>
    <p class="text-left">For klubbens medlemmer samt gjester og inviterte til OCC, er parkering og fergen gratis.
        Adresse - Bruksveien 87, 1367 Snarøya.</p>
